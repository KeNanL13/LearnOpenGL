MyOpenGLWidget::MyOpenGLWidget(QWidget *parent) :
    QOpenGLWidget (parent),
    camera(this)

{
    QSurfaceFormat format;
    format.setSamples(4);

    QTimer *timer=new QTimer;
    timer->setInterval(18);
    connect(timer,&QTimer::timeout,[=](){
        makeCurrent();
        update();
        doneCurrent();
    });
    timer->start();
    camera.init();
    lightPos=QVector3D(0,0,3);


}

MyOpenGLWidget::~MyOpenGLWidget()
{
    makeCurrent();

    doneCurrent();
}



void MyOpenGLWidget::setShape()
{

    m_Shape=Rect;
    update();
}



void MyOpenGLWidget::setWireFrame(bool isWireFrame)
{
    makeCurrent();
    if(isWireFrame)
    {

        glPolygonMode(GL_FRONT_AND_BACK,GL_LINE);
    }
    else {
        glPolygonMode(GL_FRONT_AND_BACK,GL_FILL);
    }
    update();
    doneCurrent();
}

void MyOpenGLWidget::Clear()
{
    m_Shape=None;
    makeCurrent();
    update();
    doneCurrent();
}

void MyOpenGLWidget::initializeGL()
{
    initializeOpenGLFunctions();
    shaderProgram.addShaderFromSourceFile(QOpenGLShader::Vertex,":/GLSL/shadow.vert");
    shaderProgram.addShaderFromSourceFile(QOpenGLShader::Fragment,":/GLSL/shadow.frag");
    shaderProgram.link();

    glGenVertexArrays(1,&VAO);
    glGenBuffers(1,&VBO);
    glBindVertexArray(VAO);
    glBindBuffer(GL_ARRAY_BUFFER,VBO);
    glBufferData(GL_ARRAY_BUFFER,sizeof (plane),plane,GL_STATIC_DRAW);
    glVertexAttribPointer(0,3,GL_FLOAT,GL_FALSE,sizeof(float)*5,nullptr);
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(1,2,GL_FLOAT,GL_FALSE,sizeof(float)*5,(void*)(sizeof(float)*3));
    glEnableVertexAttribArray(1);
    glBindVertexArray(0);

    glEnable(GL_DEPTH_TEST);
    shaderProgram.bind();
    projection.perspective(90,this->width()/this->height(),0.1,100);
    shaderProgram.setUniformValue("projection",projection);
    shaderProgram.setUniformValue("lightPos",lightPos);
    shaderProgram.setUniformValue("normalsMap",0);
    shaderProgram.setUniformValue("diffuseMap",1);
    texture=new QOpenGLTexture(QImage(":/Texture/brickwall.jpg"));
    textureNormal=new QOpenGLTexture(QImage(":/Texture/brickwall_normal.jpg"));
    model.setToIdentity();





}

void MyOpenGLWidget::resizeGL(int w, int h)
{
    glViewport(0,0,w,h);
}

void MyOpenGLWidget::paintGL()
{

    glClearColor(0.4f,0.5f,0.4f,0);
    glClear(GL_COLOR_BUFFER_BIT|GL_DEPTH_BUFFER_BIT);


    switch (m_Shape) {
    case Rect:

      shaderProgram.bind();
      texture->bind(1);
      textureNormal->bind(0);
      shaderProgram.setUniformValue("projection",projection);
      shaderProgram.setUniformValue("view",camera.getView());
      shaderProgram.setUniformValue("model",model);
      shaderProgram.setUniformValue("viewPos",camera.getView());
       glBindVertexArray(VAO);
       glDrawArrays(GL_TRIANGLES,0,6);
        break;
    default:
        break;
    }

}

bool MyOpenGLWidget::event(QEvent *e)
{
    camera.handle(e);
    return QWidget::event(e);
}

void MyOpenGLWidget::keyPressEvent(QKeyEvent *event)
{
        makeCurrent();
    switch (event->key()) {
    case Qt::Key_Up:
        model.scale(1.1f);
        break;
    case Qt::Key_Down:
        model.scale(0.9f);
        break;
    default:
        break;
    }

    update();
    doneCurrent();
}

void MyOpenGLWidget::drawScene(QOpenGLShaderProgram &shaderProgram)
{
   //room cube
    model.setToIdentity();
    model.scale(QVector3D(5.0f,5.0,5.0));
    shaderProgram.setUniformValue("model",model);
    glDisable(GL_CULL_FACE);
    shaderProgram.setUniformValue("reverse_normal",1);
    renderCube();
    shaderProgram.setUniformValue("reverse_normal",0);
    glEnable(GL_CULL_FACE);
    //CUBE
    model.setToIdentity();
    model.translate(QVector3D(4,-3.5,0));
    model.scale(0.5);
    shaderProgram.setUniformValue("model",model);
    renderCube();
    model.setToIdentity();
    model.translate(QVector3D(2,3,1));
    model.scale(0.75);
    shaderProgram.setUniformValue("model",model);
    renderCube();
    model.setToIdentity();
    model.translate(QVector3D(-3,-1,0));
    model.scale(0.5);
    shaderProgram.setUniformValue("model",model);
    renderCube();
    model.setToIdentity();
    model.translate(QVector3D(-1.5,1,1.5));
    model.scale(0.5);
    shaderProgram.setUniformValue("model",model);
    renderCube();
    model.setToIdentity();
    model.translate(QVector3D(-1.5,2,-3));
    model.scale(0.75);
    shaderProgram.setUniformValue("model",model);
    renderCube();
}

